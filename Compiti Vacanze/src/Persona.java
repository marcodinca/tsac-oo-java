public class Persona {
    public String nome;
    public String cognome;
    public double altezza;
    public double peso;
    public int eta;
    public char sesso;
    public String livelloAttivita;
    
    public Persona(String nome, String cognome, double altezza, double peso, int eta, char sesso, String livelloAttivita) {
        this.nome = nome;
        this.cognome = cognome;
        this.altezza = altezza;
        this. peso = peso;
        this.eta = eta;
        this.sesso = sesso;
        this.livelloAttivita = livelloAttivita;
    }
}
